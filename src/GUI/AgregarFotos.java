    private void brnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnAgregarActionPerformed
        lblSucceded.setText("");
        JFileChooser jfile = new JFileChooser();
        int resultado = jfile.showOpenDialog(this);
        
        if(resultado != JFileChooser.CANCEL_OPTION){
            File nombre = jfile.getSelectedFile();
            this.mostrarImagen(lblImagen, nombre.getAbsolutePath());
            
            txtRutaImagen.setText(nombre.getAbsolutePath());
        }
    }//GEN-LAST:event_brnAgregarActionPerformed

    private void btnSubirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubirActionPerformed
        String nombre = txtRutaImagen.getText();
        int num = lugares.countImg(VerLugar.nombreLugar)+1;
        String ruta = "IMG\\"+VerLugar.nombreLugar+"\\"+num+".jpg";
        
        Path rutaOrigen = Path.of(nombre);
        Path rutaDestino = Path.of(ruta);
        
        try {
            byte[] imageBytes = Files.readAllBytes(rutaOrigen);
            Files.write(rutaDestino,imageBytes);
            lblSucceded.setText("Imagen Cargada Correctamente");
        } catch (IOException ex) {
            Logger.getLogger(AgregarFotos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSubirActionPerformed

